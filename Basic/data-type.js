// Integer Data type
var num = 10
console.log(num)
console.log("Type is:", typeof num)


// String data type
var name = "Munna"
console.log(name)
console.log("Type is :", typeof name)

//  Boolean data type
var isMarried = false
console.log(isMarried)
console.log("Type is :", typeof isMarried)

// Null data type
var x = null
console.log(x)
console.log("Type is :", typeof x)

// Undefined
var y;
console.log(y)
console.log("Type is :", typeof y)