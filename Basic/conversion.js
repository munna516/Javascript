// Parse number form Float to Integer
var num1 = 10.5
console.log(parseInt(num1))

// Fixed to a decimal Point
var num2 = 10.68234859823
console.log(parseFloat(num2.toFixed(3)))

//  String to Integer conversion
var strNum = "10"
console.log(typeof strNum)
console.log(parseInt(strNum))
console.log(typeof parseInt(strNum))

// String to float conversion
var strNums = "10.7"
console.log(typeof strNums)
console.log(parseFloat(strNums))
console.log(typeof parseInt(strNums))